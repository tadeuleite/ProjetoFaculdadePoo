    private void jButtonProcurarNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcurarNotaActionPerformed
        NotaFiscal notaFiscal = new NotaFiscal();
        NegocioNotaFiscal nota = new NegocioNotaFiscal();
        jButtonClientesActionPerformed(evt);
        jButtonVendedoresActionPerformed(evt);

        try {
            notaFiscal.setCodigoNota(Integer.parseInt(jComboBoxNotas.getSelectedItem().toString()));
            ArrayList<NotaFiscal> nf = nota.consultarNotaFiscal(notaFiscal);

            for (NotaFiscal notaF : nf) {
                jTextFieldCodigoProcurar.setText(Integer.toString(notaF.getCodigoNota()));
                jTextFieldCodigoNota.setText(Integer.toString(notaF.getCodigoNota()));
                jTextFieldCliente.setText(notaF.getCliente().getNome());
                jTextFieldVendedor.setText(notaF.getVendedor().getNome());
                jTextFieldDataEmissao.setText(ConvercaoDate.parseDate((notaF.getDataEmissao())));
            }
            jTextFieldCliente.setEnabled(true);
            jTextFieldVendedor.setEnabled(true);
            jTextFieldDataEmissao.setEnabled(true);
            jButtonAlterar.setEnabled(true);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um código válido");

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Insira corretamente o código da nota");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonProcurarNotaActionPerformed

