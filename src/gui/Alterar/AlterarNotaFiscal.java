    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        NotaFiscal notaFiscal = new NotaFiscal();
        NegocioNotaFiscal notaF = new NegocioNotaFiscal();
        NegocioVendedor vend = new NegocioVendedor();
        NegocioCliente cli = new NegocioCliente();

        try {
            notaFiscal.setCodigoNota(Integer.parseInt(jComboBoxNotas.getSelectedItem().toString()));
            notaFiscal.setDataEmissao(parseDate(jTextFieldDataEmissao.getText()));
            notaFiscal.getCliente().setNome(jTextFieldCliente.getText());

            notaFiscal.getCliente().setNome(jComboBoxClientes.getSelectedItem().toString());
            notaFiscal.getVendedor().setNome(jComboBoxVendedores.getSelectedItem().toString());

            ArrayList<Cliente> c = cli.consultarClienteLike(notaFiscal.getCliente());
            ArrayList<Vendedor> v = vend.consultarVendedorLike(notaFiscal.getVendedor());

            for (Cliente cl : c) {
                notaFiscal.getCliente().setCpf(cl.getCpf());
            }
            for (Vendedor ve : v) {
                notaFiscal.getVendedor().setCodigo(ve.getCodigo());
            }

            int escolha = JOptionPane.showConfirmDialog(null, "Deseja alterar a nota fiscal de código: " + notaFiscal.getCodigoNota() + "?");
            if (escolha == 0) {
                notaF.alterarNotaFiscal(notaFiscal);
                JOptionPane.showMessageDialog(null, "Nota Fiscal alterada com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Não alterado");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

