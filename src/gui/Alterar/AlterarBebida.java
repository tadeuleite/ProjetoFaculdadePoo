    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        Bebida bebida = new Bebida();
        NegocioBebida beb = new NegocioBebida();

        try {
            bebida.setNome(jComboBoxBebidas.getSelectedItem().toString());
            ArrayList<Bebida> b = beb.consultarBebidaLike(bebida);

            for (Bebida bebi : b) {
                bebida.setCodigo(bebi.getCodigo());
            }
            bebida.setNome(jTextFieldNome.getText());
            bebida.setCapacidade(Integer.parseInt(jTextFieldCapacidade.getText()));
            bebida.setMarca(jTextFieldMarca.getText());
            bebida.setTipo(jTextFieldTipoBebida.getText());

            int escolha = JOptionPane.showConfirmDialog(null, "Deseja alterar o produto: " + bebida.getNome() + "?");
            if (escolha == 0) {
                beb.alterarBebida(bebida);
                JOptionPane.showMessageDialog(null, "Produto alterado com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "NÃ£o alterado");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Digite apenas nÃºmeros na capacidade");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

